    public void exibirFotoCandidato(String numeroCandidato) {
        String caminho = "Candidatos/" + numeroCandidato;
        
        try {
            URL imageUrl = getClass().getClassLoader().getResource(caminho);
            
            if (imageUrl != null) {
                ImageIcon originalIcon = new ImageIcon(imageUrl);
                
                int larguraLbl = photo.getWidth() > 0 ? photo.getWidth() : 100;
                int alturaLbl = photo.getHeight() > 0 ? photo.getHeight() : 100;
                
                Image scaledImage = originalIcon.getImage().getScaledInstance(larguraLbl, alturaLbl, Image.SCALE_SMOOTH);
                ImageIcon scaledIcon = new ImageIcon(scaledImage);
                photo.setIcon(scaledIcon);
                photo.setText("");
            } else {
                photo.setIcon(null);
                photo.setText("Foto nÃ£o encontrada");
                
                System.err.println("Imagem nÃ£o encontrada: " + caminho);
            }
        } catch (Exception e) {
            photo.setIcon(null);
            photo.setText("Erro ao carregar foto");
        }
    }
    
